#ifndef _SUB_QQ_Q_H_
#define _SUB_QQ_Q_H_

/*
  SUB_QQ_Q

  Вычитание двух рациональных дробей: result = a - b

  Параметры:
    1) NUMBQ* a — указатель на первую дробь (уменьшаемое)
    2) NUMBQ* b — указатель на вторую дробь (вычитаемое)

  Возвращает указатель на новую дробь NUMBQ* — результат вычитания,
  или NULL при ошибке (нулевой знаменатель или некорректные аргументы).
*/
NUMBQ* SUB_QQ_Q(NUMBQ* a, NUMBQ* b);

#endif
