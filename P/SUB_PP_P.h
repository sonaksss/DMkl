#ifndef _SUB_PP_P_H_
#define _SUB_PP_P_H_


/*
  SUB_PP_P

  Вычитание многочленов: вычисляет разность двух многочленов A и B (A - B).

  Параметры:
    1) NUMBP* A — указатель на первый многочлен (уменьшаемое)
    2) NUMBP* B — указатель на второй многочлен (вычитаемое)

  Функция возвращает указатель на новый многочлен NUMBP* — результат A - B,
  или NULL при ошибке (NULL-аргументы или ошибка выделения памяти).
*/
NUMBP* SUB_PP_P(NUMBP* A, NUMBP* B);

#endif
