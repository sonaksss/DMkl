/*
  DEG_P_N (P-6)
  Степень многочлена.
  NUMBP хранит степень в поле m.
  Функция просто возвращает это значение.
  Параметры:
  NUMBP* p - указатель на многочлен
  Возвращает:
  int - степень многочлена
  ОШИБКА:
  Возвращает -1 при невалидных данных.
*/

int DEG_P_N(NUMBP* p) {
    if (p == NULL || p->C == NULL || p->m < 0) {
        return -1;
    }
    return p->m;
}
