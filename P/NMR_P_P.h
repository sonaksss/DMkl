#ifndef _NMR_P_P_H_
#define _NMR_P_P_H_

/*
  NMR_P_P

  Преобразование многочлена: кратные корни в простые.
  Возвращает многочлен P / GCF(P, P'), где P' — производная P.
  Результирующий многочлен имеет те же корни, что и исходный,
  но все они простые (кратность каждого корня равна 1).

  Параметры:
    1) NUMBP* p — указатель на исходный многочлен

  Возвращает указатель на новый NUMBP — многочлен с простыми корнями,
  или NULL при ошибке (нулевой указатель на вход, нулевой многочлен,
  нулевой НОД, ошибка деления).
*/
NUMBP* NMR_P_P(NUMBP* p);

#endif
